package aula;

public class ExemploOperadoresAtribuicao {

	public static void main(String[] args) {
		
		int valor2=10;
		int valor=5;
		valor+=valor2;
		System.out.println("O novo valor é: "+valor);
		valor-=valor2;
		System.out.println("O novo valor é: "+valor);
		valor*=valor2;
		System.out.println("O novo valor é: "+valor);
		valor/=valor2;
		System.out.println("O novo valor é: "+valor);
		valor%=valor2;
		System.out.println("O novo valor é: "+valor);
		
		
			}

}
