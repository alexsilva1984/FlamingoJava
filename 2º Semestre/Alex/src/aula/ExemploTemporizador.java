package aula;

public class ExemploTemporizador {

	public static void main(String[] args) {
		
			System.out.println("Cronometro Temporizador");
			
			for (int k = 24; k >= 0; k--) {
			for (int i = 59; i >= 0; i--) {
			for (int j = 59; j >= 0; j--) {
			System.out.println(k+":"+i+":"+j);
			}
			}
		}
		}
	}

